/**
* _memset - used to fill a memory address with a constant byte
* @s: starting and ending memory location of memory to be filled
* @b: value to be copied to the memory segement
* @n: number of bytes in the memory block to be filled with the constant bytes
*
* Return: modified s
*/
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i = 0;

	while (i < n)

	{
	s[i] = b;
	i = i + 1;
	}
	return (s);
}
